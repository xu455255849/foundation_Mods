local BUILDING_FUNCTION_SECURITY_MONASTERY = {
    TypeName = "BUILDING_FUNCTION_SECURITY_MONASTERY",
    ParentType = "BUILDING_FUNCTION_WORKPLACE",
    Properties = {
    }
}

-- Activate
function BUILDING_FUNCTION_SECURITY_MONASTERY:activateBuilding(gameObject)

    -- mod:log("Embedded security function activateBuilding() called by gameobject " .. tostring(gameObject))

    -- Attach security points to the building or building part where this workplace resides
    local compPoints = gameObject:getOrCreateComponent("COMP_SECURITY_POINTS")
    compPoints.BasePoints = 2
    compPoints.WorkerPoints = 2

    -- Trigger the placement of an overlay component
    gameObject:getOrCreateComponent("COMP_SECURITY_OVERLAY_INIT")

    local compWorkplace = gameObject:getOrCreateComponent("COMP_WORKPLACE")
    compWorkplace:setWorkplaceData(self)

    return true

end


