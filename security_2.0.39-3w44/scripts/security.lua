function BUILDING_FUNCTION_SECURITY_MONASTERY:removeBuildingFunction(gameObject)

    local compPoints = gameObject:getComponent("COMP_SECURITY_POINTS")
    if compPoints then
        gameObject:removeComponent(compPoints)
    end

    return true

end

